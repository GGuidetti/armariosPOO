package br.edu.ifpr.paranavai.construtores.diagrama;


public class Curso {
	// atributos
	String nome;
	String dataDeCriacao;
	String periodo;
	
	public Curso() {
		
	}
	public Curso(String nome, String dataDeCriacao, String periodo) {
		this.nome = nome;
		this.dataDeCriacao = dataDeCriacao;
		this.periodo = periodo;
	}
	
	
}
